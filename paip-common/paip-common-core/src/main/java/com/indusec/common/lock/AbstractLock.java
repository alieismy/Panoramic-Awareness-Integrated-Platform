package com.indusec.common.lock;

import lombok.AllArgsConstructor;
import lombok.Getter;

/**
 * <p> 锁对象抽象 </p>
 *
 * <p> Description </p>
 *
 * @author: L.J
 * @email: JiangLie@Hotmail.com
 * @version: 3.0.0
 * @since: 2020/7/28 9:22
 */
@AllArgsConstructor
public class AbstractLock implements AutoCloseable {
    @Getter
    private final Object lock;

    private final DistributedLock locker;

    @Override
    public void close() throws Exception {
        locker.unlock(lock);
    }
}
